package com.iteye.weimingtom.myosotis.memorybasic;

public class Label {
	private int index;
	private int pos;
	
	public Label(int index) {
		this.index = index;
		this.pos = 0;
	}

	public int getIndex() {
		return index;
	}

	public void setIndex(int index) {
		this.index = index;
	}

	public int getPos() {
		return pos;
	}

	public void setPos(int pos) {
		this.pos = pos;
	}
}

